/**
 * Get the logo URL for a stock ticker
 * Uses Clearbit Logo API with a known ticker-to-domain mapping
 */
export function getStockLogoUrl(ticker: string): string | null {
  const domain = TICKER_TO_DOMAIN[ticker.toUpperCase()];
  if (!domain) return null;
  return `https://logo.clearbit.com/${domain}`;
}

/**
 * Get fallback initials for a ticker (max 2 chars)
 */
export function getTickerInitials(ticker: string): string {
  return ticker.substring(0, 2).toUpperCase();
}
